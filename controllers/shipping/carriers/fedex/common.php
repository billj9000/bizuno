    protected function mapAddress($addr=[])
    {
        $output = [];
        if (!empty($addr['address1']))   { $output['streetLines'][]      = $addr['address1']; }
        if (!empty($addr['address2']))   { $output['streetLines'][]      = $addr['address2']; }
        if (!empty($addr['city']))       { $output['city']               = $addr['city']; }
        if (!empty($addr['state']))      { $output['stateOrProvinceCode']= $addr['state']; }
        if (!empty($addr['postal_code'])){ $output['postalCode']         = $addr['postal_code']; }
        if (!empty($addr['country']))    { $output['countryCode']        = $addr['country']; }
        if (!empty($addr['residential'])){ $output['residential']        = $addr['residential']; }
        return $output;
    }

    protected function mapContact($addr=[])
    {
        $output = [];
        if (!empty($addr['primary_name'])){ $output['companyName'] = $addr['primary_name']; }
        if (!empty($addr['contact']))     { $output['personName']  = $addr['contact']; }
        if (!empty($addr['telephone1']))  { $output['phoneNumber'] = substr(str_replace(' ', '', clean($addr['telephone1'], 'numeric')), 0, 10); }
        if (!empty($addr['email']))       { $output['emailAddress']= $addr['email']; }
        return $output;
    }

