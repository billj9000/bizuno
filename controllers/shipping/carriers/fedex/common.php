            default:
            case 'SENDER':      $pkg['payor']['creds'] = $this->getCreds($payID);
        }
    }

    /**
     * FedEx now uses RESTful API
     * @param string $path
     * @param array $data
     * @param string $method
     * @return type
     */
    protected function queryREST($path, $data=[], $method='post')
    {
        global $io;
        msgDebug("\nEntering queryREST with path = $path and sizeof data = ".sizeof($data));
        if (empty($this->settings['token']) || $this->settings['token_date'] < time()-10) { // get a new token for today
            $this->settings['token'] = $this->getTokenREST();
            if (empty($this->settings['token'])) { return msgAdd("Error retrieving token from FedEx, all services will be unavailable!"); }
        }
        $url     = "$this->urlREST/$path";
        $opts    = ['headers'=>['authorization'=>"Bearer {$this->settings['token']}", 'x-locale'=>'en_US', 'content-type'=>'application/json']];
        $response= false;
        msgDebug("\nSending request to url $url and data:".print_r($data, true));
        for ($i=0; $i<$this->retries; $i++) {
            $response= json_decode($io->cURL($url, json_encode($data), $method, $opts), true);
            msgDebug("\nLast response is: ".print_r($response, true));
            if (!empty($response)) { break; }
        }
        if (empty($response)) { msgAdd(sprintf(lang('err_no_communication'), $this->code).' '.$this->excNotes,); }
        return $response;
    }

    /**
     * Fetch oAuth token from fedEx servers
     * @global class $io
     * @return token if successful, null if error
     */
    private function getTokenREST()
    {
        global $io;
        $urlOauth= "$this->urlREST/oauth/token";
        $opts    = ['headers'=>['Content-Type'=>'application/x-www-form-urlencoded']];
        $data    = "grant_type=client_credentials&client_id={$this->settings['rest_api_key']}&client_secret={$this->settings['rest_secret']}";
        $response= json_decode($io->cURL($urlOauth, $data, 'post', $opts), true);
        msgDebug("\nReceived back from url $urlOauth with results: ".print_r($response, true));
        if (!empty($response['errors'])) { return msgAdd("FedEx Authentication Error: ".print_r($response['errors'], true)); }
        $this->tokenUpdate($response);
        return $response['access_token'];
    }

    private function tokenUpdate($response=[])
    {
        msgDebug("\nin tokenUpdate, read token = {$response['access_token']} and expires_in = {$response['expires_in']}");
        $token    = $response['access_token'];
        $tokenDate= time()+$response['expires_in'];
        $this->settings['token']     = $token;
        $this->settings['token_date']= $tokenDate;
        $props    = getModuleCache($this->moduleID, $this->methodDir, $this->code);
        $props['settings'] = $this->settings;
        setModuleCache($this->moduleID, $this->methodDir, $this->code, $props);
    }

    /**
     *
     * @param type $transit_time
     * @param type $res
     * @return type
     */
    protected function calculateDelivery($transit_time, $res = true) {
        $today       = getdate();
        $today_date  = biz_date('Y-m-d');
        $year        = biz_date('Y');
        $day_of_week = $today['wday'] - 1;  // 0 - Monday thru 6 - Sunday
        $holidays = [
            biz_date('Y-m-d', strtotime("1st January $year")),                 // New Years Day
            biz_date('Y-m-d', strtotime("Last Monday of May $year")),          // Memorial Day
            biz_date('Y-m-d', strtotime("4th July $year")),                    // Independence Day
            biz_date('Y-m-d', strtotime("First Monday of September $year")),   // Labor Day
            biz_date('Y-m-d', strtotime("Fourth Thursday of November $year")), // Thanksgiving Day
            biz_date('Y-m-d', strtotime("25th December $year")),               // Christmas Day
            ];
        switch ($transit_time) {
            case 'ONE_DAY':   $offset = 1; break;
            case 'TWO_DAYS':  $offset = 2; break;
            case 'THREE_DAYS':$offset = 3; break;
            case 'FOUR_DAYS': $offset = 4; break;
            case 'FIVE_DAYS': $offset = 5; break;
            case 'SIX_DAYS':  $offset = 6; break;
            case 'SEVEN_DAYS':$offset = 7; break;
            case 'EIGHT_DAYS':$offset = 8; break;
            case 'NINE_DAYS': $offset = 9; break;
        }
        if     (($day_of_week + $offset) > 9) { $offset = $offset + 4; } // passed through two weekends
        elseif (($day_of_week + $offset) > 4) { $offset = $offset + 2; } // passed through one weekend
        $curDel = biz_date('Y-m-d', strtotime("+$offset days"));
        // check for holidays
        foreach ($holidays as $holiday) {
            if ($today_date <= $holiday && $curDel >= $holiday) {
                $offset++;
                $curDel = biz_date('Y-m-d', strtotime("+$offset days"));
            }
        }
        $d = getdate(strtotime("+$offset days"));
        $delivery = biz_date('Y-m-d', mktime(0, 0, 0, $d['mon'], $d['mday'], $d['year']));
        return $delivery . ' ' . ($res ? $this->lang['del_time_RE'] : $this->lang['del_time_GD']);
    }

