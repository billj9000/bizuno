    function __construct()
    {
        $this->lang = array_replace(getLang($this->moduleID), $this->lang);
        localizeLang($this->lang, $this->methodDir, $this->code);
        $this->defaults = ['order'=>90,'test_mode'=>'Y','username'=>'','password'=>'','acct_id'=>'','authorization'=>'',
            'gl_acct_c' => getModuleCache('shipping','settings','general','gl_shipping_c'),
            'gl_acct_v' => getModuleCache('shipping','settings','general','gl_shipping_v'),
            'min_weight'=>150,'ltl_class'=>125,'ltl_desc'=>'','service_types'=>'ECF','default'=>'0', 'token'=>'', 'token_date'=>''];
        $this->options = $this->getOptions();
        $this->settings= array_replace_recursive($this->defaults, getModuleCache($this->moduleID, $this->methodDir, $this->code, 'settings', []));
        $this->today   = biz_date('Y-m-d');
        $this->currency= 'USD';
        $this->test    = $this->settings['test_mode']=='Y' ? true : false;
        $this->creds   = $this->getCreds(0);
    }

    // ***************************************************************************************************************
    //                                Support functions
    // ***************************************************************************************************************
    protected function getXPO($url='', $data='', $type='auto')
    {
        global $portal;
        msgDebug("\nEntering getXPO with settings token = {$this->settings['token']} and token_date = {$this->settings['token_date']}");
        if (empty($this->settings['token']) || $this->settings['token_date'] < time()) { // get a new token for today
            $this->settings['token'] = $this->getToken();
            if (!$this->settings['token']) { msgAdd("Error retrieving token from XPO, all services will be unavailable!"); return []; }
        }
        $url .= "?testMode=" . ($this->test ? 'Y' : 'N');
        msgDebug("\nXPO Request = ".print_r($data, true));
        switch ($type) {
            case 'auto': $reqType = (empty($data)) ? 'get' : 'post'; break;
            default:     $reqType = $type;
        }
        switch ($reqType) { // set the headers
            case 'get':
                $opts = ['headers'=>['Authorization'=>"Bearer {$this->settings['token']}"]];
                break;
            case 'put':
            case 'post':
                $opts = ['headers'=>['Authorization'=>"Bearer {$this->settings['token']}", 'cache-control'=>"no-cache", 'content-type'=>"application/json"]];
                break;
        }
        $response= json_decode($portal->cURL($url, $data, $reqType, $opts), true);
        if (empty($response)) { return; }
        msgDebug("\nParsed XPO response = ".print_r($response, true));
        if (!empty($response['error'])) { msgAdd("XPO Error: {$response['error']['errorCode']}: {$response['error']['message']}"); return; }
        if ($response['code'] >= 200 && $response['code'] <= 300) { return $response; }
        msgAdd("XPO reponse error, HTML Error Code ".$response['code']);
        return $response;
    }

    private function getToken()
    {
        global $portal;
        $opts    = ['headers'=>['authorization'=>"Basic {$this->settings['authorization']}"]];
        $request = ['grant_type'=>"password", 'username'=>$this->settings['username'], 'password'=>$this->settings['password']];
        $response= json_decode($portal->cURL('https://api.ltl.xpo.com/token', $request, 'post', $opts), true);
        if (!empty($response['error'])) { msgAdd("XPO Error: {$response['error']}: {$response['error_description']}"); return; }
        $this->settings['token']     = $response['access_token'];
        $this->settings['token_date']= time()+$response['expires_in'];
        $methProps = getModuleCache($this->moduleID, $this->methodDir, $this->code);
        $methProps['settings'] = $this->settings;
        setModuleCache($this->moduleID, $this->methodDir, $this->code, $methProps);
        return $response['access_token'];
    }

    protected function getCreds($bID=0)
    {
        msgDebug("\nWorking with getCreds store id = $bID");
        $multi = getModuleCache('contacts', $this->moduleID, $this->code, 'multi_store');
        if (!$bID || !isset($multi[$bID])) { return; }
        $values = $multi[$bID];
        if (isset($values['acct_number'])) { $this->settings['acct_number'] = $values['acct_number']; }
        if (isset($values['ltl_acct_num'])){ $this->settings['ltl_acct_num']= $values['ltl_acct_num']; }
        if (isset($values['meter_number'])){ $this->settings['meter_number']= $values['meter_number']; }
        if (isset($values['auth_key']))    { $this->settings['auth_key']    = $values['auth_key']; }
        if (isset($values['auth_pw']))     { $this->settings['auth_pw']     = $values['auth_pw']; }
        if (isset($values['sp_hub']))      { $this->settings['sp_hub']      = $values['sp_hub']; }
    }

