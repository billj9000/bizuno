        'acronym' => 'XPO',
        'description' => 'This method provides discounted quotes based on your XPO contracted account. The credentials (user name, password, and AcctID) must be applied for through your local XPO Representative. Labels must be generated by hand or through their website, <a href="http://www.xpo.com" target="_blank">www.xpo.com</a>.',
        'username' => 'Enter the XPO user name used to register with XPO.',
