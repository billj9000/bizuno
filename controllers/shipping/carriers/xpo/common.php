                'COLLECT'         => $this->lang['collect']],
            'LTLClasses'=> ['0'=>lang('select'),'50'=>'50','55'=>'55','60'=>'60','65'=>'65','70'=>'70','77'=>'77.5','85'=>'85',
                '92'=>'92.5','100'=>'100','110'=>'110','125'=>'125','150'=>'150','175'=>'175','200'=>'200','250'=>'250','300'=>'300']];
    }

    protected function mapAddress($addr=[])
    {
        $output = [];
        if (!empty($addr['primary_name'])){ $output['contactInfo']['companyName']= $addr['primary_name']; }
        if (!empty($addr['contact']))     { $output['contactInfo']['fullName']   = !empty($addr['contact']) ? $addr['contact'] : $addr['primary_name']; }
        if (!empty($addr['telephone1']))  { $output['contactInfo']['phone']      = ['countryCd'=>'1','phoneNbr'=>$this->setPhone($addr['telephone1'])]; } // goofy phone number format
        if (!empty($addr['email']))       { $output['contactInfo']['email']      = ['emailAddr'=>$addr['email']]; }
        if (!empty($addr['primary_name'])){ $output['address']['careOfName']     = $addr['primary_name']; }
        if (!empty($addr['address1']))    { $output['address']['addressLine1']   = $addr['address1']; }
        if (!empty($addr['address2']))    { $output['address']['addressLine2']   = $addr['address2']; }
        if (!empty($addr['city']))        { $output['address']['cityName']       = $addr['city']; }
        if (!empty($addr['state']))       { $output['address']['stateCd']        = $addr['state']; }
        if (!empty($addr['postal_code'])) { $output['address']['postalCd']       = $addr['postal_code']; }
        if (!empty($addr['country']))     { $output['address']['countryCd']      = $addr['country']=='CA' ? 'CN' : $addr['country']; } // they goofed the ISO2 Canada code
//      if (!empty($addr['residential'])) { $output['address']['residential']    = $addr['residential']; } // Not sure how to handle residential
        return $output;
    }

    protected function setPhone($value)
    {
        $alpha = preg_replace("/[^0-9]/", "", $value);
        return substr($alpha, 0, 3).'-'.substr($alpha, 3, 7);
    }
}
